package com.flsh.model;

public class NumberStudent {
	private int number_student;
	private float percentage_student;
	
	public int getNumber_student() {
		return number_student;
	}
	
	public void setNumber_student(int number_student) {
		this.number_student = number_student;
	}
	
	public float getPercentage_student() {
		return percentage_student;
	}
	
	public void setPercentage_student(float percentage_student) {
		this.percentage_student = percentage_student;
	}
}
